public class location {

    private double latitude;
    private double longitude;
    private String city;
    private String country;

    public location(){}

    public double getLatitude() {
        return latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public String getCity() {
        return city;
    }

    public String getCountry() {
        return country;
    }
}
